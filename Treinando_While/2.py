"""
Faça um programa que imprima 7 vezes na tela do usuario a frase:
"Você consegue" utilizando o while

"""

i = 1  # cria uma variavel e dá o valor 1  a ela
while i <= 7:  # define um loop "enquanto i for menor ou = a 7"
    print("Você consegue")
    i += 1  # atualiza o valor da variavel para sempre +1
